$('#toggle').click(function(){
	var value = $('#toggle').attr('value');
	$('#freewareimage').toggle('fast');

	if(value == 'Hide Image'){
		$('#toggle').attr('value','Show Image');
	} else if (value == 'Show') {
		$('#toggle').attr('value','Hide Image');
	}
});
